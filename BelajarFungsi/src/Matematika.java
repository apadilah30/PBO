public class Matematika {
    int nilai1;
    int nilai2;
    
    int tambah(){
        int hasil = nilai1+nilai2;
        return hasil;
    }
    
    int kurang(){
        int hasil = nilai1-nilai2;
        return hasil;
    }
    
    int kali(){
        int hasil = nilai1*nilai2;
        return hasil;
    }
    
    int bagi(){
        int hasil = nilai1/nilai2;
        return hasil;
    }
}
